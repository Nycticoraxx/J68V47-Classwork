package ClassExcerises.HowTo;

public class javaOperators {

    public static void main (String [] args){


        // We can store a sum in a variable then display it
        int x = 1 + 3;
        System.out.println("The sum is: " + x);

        //We can display the sum directly by using brackets
        // Notice the first '+' is a concatenation and the second 'x' is an operator.
        System.out.println("The sum is: " + (1 + 3) );



            //OPERATORS
// '+' add
// '-' sub
// '/' divide
// '*' times
// '++' increment
// '--' decrement
// '%' remainder

            //OPERATORS AUGMENTED
// '/=' division
// '%=' remainder or modulus
// '++=' increment
// '--=' decrement
// '*=' multiplication
// '/=' division
// '+=' addition
// '-=' subtraction
    }
}
