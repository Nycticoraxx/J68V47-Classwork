package ClassExcerises.HowTo;

public class howToMakeForLoop {
    public static void main (String [] args) {
        for(int i=0; i<=10; i++){
            System.out.println(i);
        }
    System.out.println("This is a for loop!");
    }
}
//int is an integer (a whole number)
//'i' is a variable, a letter that represents a number or several numbers
//for runs the code over and over until the loop is finished (until i=10)
//i++ adds 1 to the count on each loop
//i-- subtracts 1 to the count on each loop
//System.out.println() displays text at the end of each loop when in the loops curly brackets
//System.out.println() can also be used to print text outside of brackets
